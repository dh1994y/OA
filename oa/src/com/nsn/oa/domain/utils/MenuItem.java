package com.nsn.oa.domain.utils;
/**
 * 带有url链接的子菜单项
 * @author Administrator
 *
 */
public class MenuItem {
	
	private String name;
	private String url;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
